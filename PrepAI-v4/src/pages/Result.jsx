import { useState, useEffect } from 'react';
import { BarChart, Bar, XAxis, YAxis, Tooltip, ResponsiveContainer } from 'recharts';
import { ScoreRing, Badge, S, ThemeToggle, Confetti } from '../components/shared';
import { getColor, getVerdict } from '../utils/analysis';
import { ROLES, ALL_ROLES } from '../utils/questions';
import { useApp } from '../context/AppContext';

function exportToPDF(answers, role, avg) {
  const roleInfo = ALL_ROLES[role] || ROLES[role];
  const roleLabel = roleInfo?.label || role;
  const date = new Date().toLocaleDateString('en-US', { month: 'long', day: 'numeric', year: 'numeric' });
  const rows = answers.map((a, i) => `
    <div style="page-break-inside:avoid;margin-bottom:24px;border:1px solid #e0d8f0;border-radius:10px;padding:18px;">
      <div style="font-size:11px;color:#9B8EC7;letter-spacing:2px;margin-bottom:6px;">Q${i+1} · ${a.question.cat} · ${a.question.diff}</div>
      <div style="font-size:14px;font-weight:600;color:#2A1F3D;margin-bottom:10px;">${a.question.q}</div>
      <div style="font-size:12px;color:#5A4E6E;margin-bottom:10px;line-height:1.6;"><strong>Your answer:</strong> ${a.answer}</div>
      <div style="display:flex;gap:16px;">
        <span style="background:#3D8B5E20;color:#3D8B5E;border-radius:6px;padding:3px 10px;font-size:12px;">Score: ${a.feedback.score}/100</span>
        <span style="background:#9B8EC720;color:#9B8EC7;border-radius:6px;padding:3px 10px;font-size:12px;">${a.feedback.verdict}</span>
      </div>
      ${a.feedback.model_answer ? `<div style="margin-top:10px;background:#F2EAE0;border-radius:8px;padding:12px;font-size:12px;color:#5A4E6E;"><strong>Model answer:</strong> ${a.feedback.model_answer}</div>` : ''}
    </div>
  `).join('');

  const html = `<!DOCTYPE html><html><head><meta charset="UTF-8">
  <title>GenieBuilder Session Report</title>
  <style>
    body{font-family:'Segoe UI',sans-serif;color:#2A1F3D;max-width:760px;margin:0 auto;padding:32px;}
    h1{font-size:28px;color:#9B8EC7;}
  </style>
  </head><body>
  <h1>GenieBuilder — Session Report</h1>
  <p style="color:#5A4E6E;margin-bottom:8px;">${roleLabel} · ${date}</p>
  <div style="background:#9B8EC720;border-radius:10px;padding:16px;margin-bottom:28px;display:flex;gap:32px;">
    <div><div style="font-size:11px;color:#9B8EC7;letter-spacing:2px;">AVG SCORE</div><div style="font-size:28px;font-weight:700;color:${avg >= 75 ? '#3D8B5E' : avg >= 50 ? '#A06820' : '#B84040'}">${avg}%</div></div>
    <div><div style="font-size:11px;color:#9B8EC7;letter-spacing:2px;">VERDICT</div><div style="font-size:22px;font-weight:700;color:#2A1F3D">${getVerdict(avg)}</div></div>
    <div><div style="font-size:11px;color:#9B8EC7;letter-spacing:2px;">QUESTIONS</div><div style="font-size:22px;font-weight:700;color:#2A1F3D">${answers.length}</div></div>
  </div>
  ${rows}
  <p style="text-align:center;color:#9488A8;font-size:11px;margin-top:32px;">Generated by GenieBuilder · Keep practicing!</p>
  </body></html>`;

  const blob = new Blob([html], { type: 'text/html' });
  const url  = URL.createObjectURL(blob);
  const a    = document.createElement('a');
  a.href = url; a.download = `GenieBuilder-Report-${Date.now()}.html`;
  a.click(); URL.revokeObjectURL(url);
}

const CustomTooltip = ({ active, payload, label }) => {
  if (active && payload && payload.length) {
    return (
      <div style={{
        background: 'var(--bg-card)', border: '1px solid var(--border)',
        borderRadius: 8, padding: '8px 14px', fontSize: 12, color: 'var(--text-primary)',
      }}>
        <div style={{ fontWeight: 600 }}>{label}</div>
        <div style={{ color: getColor(payload[0].value) }}>{payload[0].value}/100</div>
      </div>
    );
  }
  return null;
};

export default function Result({ answers, role, onRetry, onHome, onDashboard, onMNC }) {
  const { theme, toggleTheme } = useApp();
  const avg  = Math.round(answers.reduce((s, a) => s + a.feedback.score, 0) / answers.length);
  const cats = answers.map(a => ({ name: a.question.cat, score: a.feedback.score }));
  const best = Math.max(...answers.map(a => a.feedback.score));
  const [showConfetti, setShowConfetti] = useState(false);
  const [expandedIdx, setExpandedIdx] = useState(null);

  useEffect(() => {
    if (avg >= 85) {
      setTimeout(() => setShowConfetti(true), 400);
      setTimeout(() => setShowConfetti(false), 5000);
    }
  }, [avg]);

  return (
    <div style={{ minHeight: '100vh', background: 'var(--bg-base)' }}>
      <Confetti show={showConfetti} />

      {/* Nav */}
      <nav className="resp-nav" style={{
        position: 'sticky', top: 0, zIndex: 100,
        background: 'var(--bg-card)', borderBottom: '1px solid var(--border)',
        padding: '0 28px', display: 'flex', alignItems: 'center',
        justifyContent: 'space-between', height: 58,
      }}>
        <span style={{ fontFamily: 'var(--font-display)', fontSize: 17, fontWeight: 600, color: 'var(--text-primary)' }}>
          Session Report
        </span>
        <div className="resp-wrap" style={{ display: 'flex', gap: 8, alignItems: 'center' }}>
          <ThemeToggle theme={theme} onToggle={toggleTheme} />
          <button onClick={() => exportToPDF(answers, role, avg)} style={{
            background: 'var(--accent-a-dim)', border: '1px solid var(--accent-a)',
            color: 'var(--accent-a)', borderRadius: 8, padding: '7px 14px',
            fontSize: 12, cursor: 'pointer', fontFamily: 'var(--font-body)',
          }}>
            ↓ Export
          </button>
          <button onClick={onDashboard} style={{
            background: 'var(--bg-surface)', border: '1px solid var(--border)',
            color: 'var(--text-secondary)', borderRadius: 8, padding: '7px 14px',
            fontSize: 12, cursor: 'pointer',
          }}>Dashboard</button>
          <button onClick={onRetry} style={{
            background: 'var(--accent-a)', color: 'var(--bg-card)',
            border: 'none', borderRadius: 8, padding: '7px 18px',
            fontSize: 13, fontWeight: 600, cursor: 'pointer', fontFamily: 'var(--font-body)',
          }}>Retry</button>
          <button onClick={onHome} style={{
            background: 'transparent', border: 'none',
            color: 'var(--text-muted)', fontSize: 12, cursor: 'pointer', padding: '7px 8px',
          }}>Home</button>
        </div>
      </nav>

      <div className="resp-padding" style={{ maxWidth: 820, margin: '0 auto', padding: '32px 20px', animation: 'fadeUp .5s ease' }}>

        {/* High score banner */}
        {avg >= 85 && (
          <div style={{
            background: 'linear-gradient(135deg, var(--success-dim), var(--accent-c-dim))',
            border: '1px solid var(--success)',
            borderRadius: 14, padding: '16px 24px', marginBottom: 24,
            textAlign: 'center', animation: 'scaleIn .5s ease',
          }}>
            <div style={{ fontSize: 24, marginBottom: 4 }}>🎉</div>
            <div style={{ fontFamily: 'var(--font-display)', fontSize: 18, color: 'var(--success)', fontWeight: 600 }}>
              Outstanding Performance!
            </div>
            <div style={{ fontSize: 13, color: 'var(--text-secondary)', marginTop: 4 }}>
              You scored {avg}% — top tier interview readiness
            </div>
          </div>
        )}

        {/* Summary card */}
        <div className="resp-card-row" style={{
          ...S.card, padding: '28px', marginBottom: 20,
          display: 'flex', alignItems: 'center', gap: 32, flexWrap: 'wrap',
        }}>
          <ScoreRing score={avg} size={150} />
          <div style={{ flex: 1, minWidth: 220 }}>
            <div style={{ ...S.label, marginBottom: 8 }}>Overall Performance</div>
            <div style={{
              fontFamily: 'var(--font-display)', fontSize: 30, fontWeight: 700,
              color: getColor(avg), marginBottom: 4,
            }}>{getVerdict(avg)}</div>
            <div style={{ color: 'var(--text-muted)', fontSize: 13, marginBottom: 20 }}>
              {(ALL_ROLES[role] || ROLES[role])?.label || role} · {answers.length} questions
            </div>
            <div style={{ display: 'grid', gridTemplateColumns: 'repeat(3, 1fr)', gap: 10 }}>
              {[['Avg Score', avg + '%'], ['Questions', answers.length], ['Best', best + '%']].map(([l, v]) => (
                <div key={l} style={{
                  background: 'var(--bg-surface)', borderRadius: 10,
                  padding: '12px', textAlign: 'center',
                }}>
                  <div style={{ ...S.label, marginBottom: 4 }}>{l}</div>
                  <div style={{
                    fontFamily: 'var(--font-mono)', fontSize: 16,
                    color: 'var(--text-primary)', fontWeight: 700,
                  }}>{v}</div>
                </div>
              ))}
            </div>
          </div>
        </div>

        {/* Mobile export banner */}
        <div className="show-mobile-export" style={{
          display: 'none', background: 'var(--accent-a-dim)',
          border: '1px solid var(--accent-a)', borderRadius: 12,
          padding: '14px 20px', marginBottom: 20,
          flexDirection: 'row', alignItems: 'center', justifyContent: 'space-between', gap: 12,
        }}>
          <div style={{ fontSize: 13, color: 'var(--accent-a)', fontWeight: 600 }}>
            📄 Save your session report
          </div>
          <button
            onClick={() => exportToPDF(answers, role, avg)}
            style={{
              background: 'var(--accent-a)', color: 'var(--bg-card)',
              border: 'none', borderRadius: 8, padding: '9px 18px',
              fontSize: 13, fontWeight: 700, cursor: 'pointer',
              fontFamily: 'var(--font-body)', whiteSpace: 'nowrap',
            }}
          >
            ↓ Export PDF
          </button>
        </div>

        {/* Bar chart */}
        <div style={{ ...S.card, padding: '22px', marginBottom: 20 }}>
          <div style={{ ...S.label, marginBottom: 16 }}>Score by Category</div>
          <div style={{ height: 150 }}>
            <ResponsiveContainer width="100%" height="100%">
              <BarChart data={cats} margin={{ top: 5, right: 10, left: -20, bottom: 5 }}>
                <XAxis dataKey="name" tick={{ fill: 'var(--text-muted)', fontSize: 10, fontFamily: 'var(--font-mono)' }} axisLine={false} tickLine={false} />
                <YAxis domain={[0, 100]} tick={{ fill: 'var(--text-muted)', fontSize: 9 }} axisLine={false} tickLine={false} />
                <Tooltip content={<CustomTooltip />} cursor={{ fill: 'var(--accent-a-dim)' }} />
                <Bar dataKey="score" fill="var(--accent-a)" radius={[5, 5, 0, 0]} />
              </BarChart>
            </ResponsiveContainer>
          </div>
        </div>

        {/* Company Readiness Graph Link */}
        <div className="resp-padding" style={{ ...S.card, padding: '22px', marginBottom: 20, display: 'flex', justifyContent: 'space-between', alignItems: 'center', flexWrap: 'wrap', gap: 16 }}>
          <div>
            <div style={{ ...S.label, marginBottom: 4 }}>Company Readiness</div>
            <div style={{ fontSize: 13, color: 'var(--text-secondary)' }}>
              See how your {avg}% applies to top tech giants.
            </div>
          </div>
          <button onClick={onMNC} style={{
            background: 'var(--bg-surface)', border: '1px solid var(--accent-a)',
            color: 'var(--accent-a)', borderRadius: 10, padding: '10px 18px',
            fontSize: 14, fontWeight: 600, cursor: 'pointer', fontFamily: 'var(--font-body)'
          }}>
            View MNC Rankings →
          </button>
        </div>

        {/* Per-question review */}
        <div style={{ ...S.label, marginBottom: 14 }}>Question-by-Question Review</div>
        <div style={{ display: 'flex', flexDirection: 'column', gap: 12 }}>
          {answers.map((a, i) => {
            const open = expandedIdx === i;
            const filler = a.feedback.filler_words;
            return (
              <div key={i} style={{
                ...S.card,
                border: `1px solid ${getColor(a.feedback.score)}25`,
                overflow: 'hidden', transition: 'box-shadow .2s',
              }}>
                {/* Collapsed header — always visible */}
                <div
                  onClick={() => setExpandedIdx(open ? null : i)}
                  style={{
                    padding: '18px 22px', cursor: 'pointer',
                    display: 'flex', alignItems: 'center', gap: 14,
                  }}
                >
                  <div style={{
                    width: 44, height: 44, borderRadius: '50%',
                    background: `${getColor(a.feedback.score)}18`,
                    border: `2px solid ${getColor(a.feedback.score)}`,
                    display: 'flex', alignItems: 'center', justifyContent: 'center',
                    fontFamily: 'var(--font-mono)', fontSize: 14, fontWeight: 700,
                    color: getColor(a.feedback.score), flexShrink: 0,
                  }}>
                    {a.feedback.score}
                  </div>
                  <div style={{ flex: 1, minWidth: 0 }}>
                    <div style={{ display: 'flex', gap: 6, marginBottom: 4, flexWrap: 'wrap' }}>
                      <Badge text={a.question.cat} />
                      <Badge text={a.question.diff}
                        color={a.question.diff === 'Hard' ? 'var(--danger)' : a.question.diff === 'Medium' ? 'var(--warning)' : 'var(--success)'}
                      />
                      {filler?.total > 0 && <Badge text={`${filler.total} fillers`} color="var(--warning)" />}
                    </div>
                    <div style={{
                      fontSize: 13, color: 'var(--text-primary)',
                      whiteSpace: 'nowrap', overflow: 'hidden', textOverflow: 'ellipsis',
                    }}>
                      {a.question.q}
                    </div>
                  </div>
                  <div style={{ color: 'var(--text-muted)', fontSize: 12, flexShrink: 0 }}>
                    {open ? '▲' : '▼'}
                  </div>
                </div>

                {/* Expanded detail */}
                {open && (
                  <div style={{
                    borderTop: '1px solid var(--border)', padding: '18px 22px',
                    animation: 'fadeIn .25s ease',
                  }}>
                    <div style={{ fontSize: 13, color: 'var(--text-secondary)', lineHeight: 1.65, marginBottom: 14 }}>
                      <strong style={{ color: 'var(--text-primary)' }}>Your answer:</strong> {a.answer}
                    </div>
                    {a.feedback.model_answer && (
                      <div style={{
                        background: 'var(--bg-surface)', borderRadius: 10, padding: '14px',
                        marginBottom: 14, border: '1px solid var(--border)',
                      }}>
                        <div style={{ fontSize: 10, color: 'var(--accent-a)', letterSpacing: 2, fontFamily: 'var(--font-mono)', marginBottom: 6 }}>
                          MODEL ANSWER
                        </div>
                        <div style={{ fontSize: 12, color: 'var(--text-secondary)', lineHeight: 1.7 }}>
                          {a.feedback.model_answer}
                        </div>
                      </div>
                    )}
                    <div style={{ display: 'grid', gridTemplateColumns: '1fr 1fr', gap: 10, marginBottom: 10 }}>
                      <div style={{ background: 'var(--success-dim)', borderRadius: 8, padding: '12px' }}>
                        <div style={{ fontSize: 10, color: 'var(--success)', letterSpacing: 2, fontFamily: 'var(--font-mono)', marginBottom: 6 }}>STRENGTHS</div>
                        {a.feedback.strengths.map((s, j) => (
                          <div key={j} style={{ fontSize: 11, color: 'var(--text-secondary)', marginBottom: 4, lineHeight: 1.5 }}>• {s}</div>
                        ))}
                      </div>
                      <div style={{ background: 'var(--warning-dim)', borderRadius: 8, padding: '12px' }}>
                        <div style={{ fontSize: 10, color: 'var(--warning)', letterSpacing: 2, fontFamily: 'var(--font-mono)', marginBottom: 6 }}>IMPROVE</div>
                        {a.feedback.improvements.map((s, j) => (
                          <div key={j} style={{ fontSize: 11, color: 'var(--text-secondary)', marginBottom: 4, lineHeight: 1.5 }}>• {s}</div>
                        ))}
                      </div>
                    </div>
                    <div style={{ fontSize: 11, color: 'var(--text-muted)', fontStyle: 'italic' }}>
                      💡 {a.feedback.tip}
                    </div>
                  </div>
                )}
              </div>
            );
          })}
        </div>
      </div>
    </div>
  );
      }
